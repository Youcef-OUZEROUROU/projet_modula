Cependant, étant donné que certains de nos prestataires de service sont situés dans des pays en dehors de l’Union Européenne (« Pays Tiers »), nous transférons certaines de vos Données dans des Pays Tiers.


Cela peut notamment être le cas vers des Pays Tiers pour lesquels la Commission Européenne n’a pas évalué le niveau de conformité.


Dans un tel cas, nous nous assurons que ce transfert est effectué en conformité avec la réglementation applicable et garantisse un niveau de protection adéquat de votre vie privée et de vos droits fondamentaux (notamment par l’utilisation de clauses contractuelles types de la Commission Européenne).


Sur simple demande formulée à notre Délégué à la protection des données, nous pouvons vous fournir davantage d’informations quant à ces transferts.


9. MESURES DE SECURITE<br>


Nous nous engageons à mettre en œuvre les mesures techniques et organisationnelles appropriées afin de garantir un niveau de sécurité adapté au risque encouru pour les droits et libertés des personnes physiques dans le cadre des traitements visés au point 2.


Ces mesures sont définies en prenant compte de l'état des connaissances, des coûts de mise en œuvre et de la nature, de la portée, du contexte et des finalités du traitement ainsi que des risques identifiés.


Par ailleurs, nous vous informons que nous respectons la norme de sécurité de l’industrie des cartes de paiement PCI DSS ; gage de notre expertise en termes de sécurité.


10. DECISION AUTOMATISEE ET PROFILAGE<br>


Nous mettons en œuvre des décisions fondées exclusivement sur un traitement automatisé (notamment du profilage) produisant des effets juridiques ou vous affectant de manière significative.


Ces décisions sont nécessaires à la conclusion ou à l'exécution du contrat entre vous et nous.<br>


Ainsi, nous mettons en œuvre une décision automatisée afin d’identifier les clients et les commandes éligibles au « paiement en 4 x ».


Cette décision est fondée sur l’analyse de différentes variables relatives notamment au type de produits ou de services commandés ainsi qu’au profil du client.


Si nous considérons, au regard de ces variables, que le risque de fraude et/ou d’impayés est trop important, une telle modalité de paiement ne vous sera pas proposée.


Concernant les décisions automatisées et le profilage, nous vous assurons le droit d'obtenir une intervention humaine, d’exprimer votre point de vue auprès de la ressource qui aura été désignée pour traiter votre dossier et de contester la décision automatique qui vous aura été opposée. 

