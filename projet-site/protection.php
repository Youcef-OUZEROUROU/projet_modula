7.1. Qu’est-ce qu’un cookie ?


A l’occasion de la consultation d’un site internet, un éditeur de site internet, tel que Cdiscount, peut être amené, sous réserve de vos choix, à déposer sur votre terminal (ordinateur, smartphone, tablette), grâce à votre navigateur, un fichier texte.


Ce fichier texte est un cookie. Il va permettre à Cdiscount, pendant la durée de validité ou d’enregistrement du cookie, d’identifier votre terminal lors de vos prochaines visites. 


Seul l’émetteur d'un cookie est susceptible de lire ou de modifier les informations contenues dans ce cookie.


Vous trouverez ci-dessous les informations relatives aux cookies susceptibles d’être déposés sur votre terminal lorsque vous visitez des pages du site www.cdiscount.com, soit par Cdiscount, soit par des tiers, ainsi que les moyens vous permettant de supprimer/refuser l’inscription de ces cookies sur votre terminal.


